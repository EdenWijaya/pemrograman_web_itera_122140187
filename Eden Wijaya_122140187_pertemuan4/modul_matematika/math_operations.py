phi = 3.14

def hitung_luas_persegi (s):
    return s * s

def hitung_keliling_persegi (s):
    return 4 * s

def hitung_luas_persegi_panjang (p, lebar):
    return p * lebar

def hitung_keliling_persegi_panjang (p, lebar):
    return 2 * (p + lebar)

def hitung_luas_lingkaran (r):
    return phi * r * r

def hitung_keliling_lingkaran (r):
    return 2 * phi * r

def celsius_to_fahrenheit(celsius):
    return (celsius * 9/5) + 32

def celsius_to_kelvin(celsius):
    return celsius + 273.15